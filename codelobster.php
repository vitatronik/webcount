    <p><span style="font-weight: bold;">Существует множество веб-редакторов.</span><br/>
    Сегодня я хочу рассказать об одном из них.
     А именно Веб-редактор <span style="font-weight: bold;">Сodelobster</span><br/>
      Данный редактор достаточно очень мощная среда программирования К сожалению совместимо только с операционной системой indows.  Функционал редактор очень высокий. На нём можно делать достаточно сложные проекты.  Редактор codelobster можно скачать с официального сайта.  Поставляются в 3 версиях версия free, light и professional.  Free это свободно распространяемое версия. Но она имеет очень небольшой функционал. Для нормальной работы Я советую пользоваться версией Light.  Более подробно о различие версий Вы можете прочитать на сайте http://codelobster.com//. Вкратце скажу что версия Light вполне достаточно для работы. Профессиональная версия стоит гораздо дороже и главное отличие от версии Light  это автодополнение кода функций в CMS движках и фреймворках.</p>
     
